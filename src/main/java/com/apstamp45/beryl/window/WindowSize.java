package com.apstamp45.beryl.window;

/**
 * Stores the window size.
 * Used to make sure the window size
 * can only be changed by the Window
 * and WindowEvent classes.
 */
public class WindowSize {

    /**
     * The window's width.
     */
    protected static int width;

    /**
     * The window's height.
     */
    protected static int height;
}
